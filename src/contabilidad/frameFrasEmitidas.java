    public static void calculaFactura(){
      
       String texMo = textMO.getText();
       if(texMo.length()== 0){texMo = "0";}
      String texMate = textMat.getText();
         if(texMate.length()== 0){texMate = "0";}
       String texTip = textTipo.getText();
        if(texTip.length()== 0){texTip = "0";}
       
      
         
            try{

                float mo = Float.valueOf(texMo.trim()).floatValue();
                float i = Float.valueOf(texTip.trim()).floatValue();
                float ma = Float.valueOf(texMate.trim()).floatValue();
                float b = mo + ma;
                float iva = b*i/100;
                float total = b + iva;
                float[] resultados = {iva, total};
                textIva.setText(Float.toString(resultados[0]));
                textTotal.setText(Float.toString(resultados[1]));
                textBase.setText(Float.toString(b));

            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null,"Los valores decimales se separan con un punto ","Debes introducir un  valor numérico",2);
                textBase.setText("");
                textIva.setText("");
                textTotal.setText("");
                textMO.setText("");
                textMat.setText("");
       }  
        
        
        
        
      
    }
    ////////////////////////BORRA LA FACTURA QUE HAY EN EL CAMPO EDITAR///////////////////////////////////////
    private static void borraFacturaEmit(){
        
        if(textEditFra.getText().length() != 0){
            BDControl bd = new BDControl();
            boolean fraEncontrada = false;
            
            
            
            
            try {
                ResultSet rs = bd.leeFacturasEmitidas();
                
                while(rs.next()){
                    if(rs.getString("numFactura").equals(textEditFra.getText())){
                   
                     fraEncontrada = true;
                    }
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex, "No se ha podido leer el listado de Facturas emitidas",1);
            }
            if(fraEncontrada == true){
                
                int borrado = JOptionPane.showConfirmDialog(null,"Estas a punto de modificar los datos correspondientes a la Factura num.: " +textEditFra.getText() );
           
                if(borrado==0){
                    try{
                                String periodo = bd.leePeriodo();
                        String sql = "DELETE FROM factemi" + periodo + " WHERE numFactura = '" + textEditFra.getText()+"'" ;

                        bd.ejecutaUpdate(sql);
                        
                          //REESCRIBE LA TABLA DE FACTURAS RECIBIDAS
                        if(radioEditFra.isSelected()==false){  
                           frameFrasEmitidas ffe = new frameFrasEmitidas();
                           ffe.dispose();
                           ffe.setVisible(true);
                        }
                    }catch(Exception e){
                        JOptionPane.showMessageDialog(null,e,"Ha ocurrido un error",1);
                    }        
                }
            }else{
                JOptionPane.showMessageDialog(null,"La factura num.: "+ textEditFra.getText()+ " no existe.","Resultado de la búsqueda",1);
            }
            
            
            
        }else{
            JOptionPane.showMessageDialog(null,"Hay que introducir un número de factura","Atención",1);
        }
    }
