    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        //ASIGNAMOS A CADA CAMPO D TEXTO EL VALOR LEIDO DE LA BASE DE DATOS
        if(textEditFra.getText().length() != 0){
            BDControl bd = new BDControl();
            ResultSet rsCli = bd.leeClientes();
            boolean facturaEncontrada = false;
            
           try {
                ResultSet rs = bd.leeFacturasEmitidas();
                
                
                while(rs.next()){
                    if(rs.getString("numFactura").equals(textEditFra.getText())){
                      textNumCli.setText(rs.getString("id_cliente"));
                      textNumFra.setText(rs.getString("numFactura"));
                      
                      textBase.setText(rs.getString("base"));
                      textTipo.setText(rs.getString("tipo"));
                      textIva.setText(rs.getString("iva"));
                      textTotal.setText(rs.getString("total"));
                      textVto.setText(rs.getString("vto"));
                      textMat.setText(rs.getString("material"));
                      textMO.setText(rs.getString("mo"));
                      radioEditFra.setSelected(true);
                      radioEditFra.setVisible(true);
                      
                     
                      //BUSCAMOS N LA TABLA CLIENTES LOS DATOS CORRESPONDIENTES AL ID
                      while(rsCli.next()){
                          if(rsCli.getString("id_cliente").equals(rs.getString("id_cliente"))){
                                String ni = rsCli.getString("nif");
                            textNif.setText(ni);
                            String nombr = rsCli.getString("nombre");
                            comboCli.setSelectedItem(nombr);
                         }
                      }
                      
                      
                     facturaEncontrada=true;
                   
                    }
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,ex, "No se ha podido leer el listado de Facturas emitidas",1);
            
            }if(facturaEncontrada == false){
                JOptionPane.showMessageDialog(null,"La factura num.: "+ textEditFra.getText()+ " no existe.","Resultado de la búsqueda",1);
            }
            
            
            
        } else{ 

            JOptionPane.showMessageDialog(null,"Hay que introducir un número de factura","Atención",1);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

