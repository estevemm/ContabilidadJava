    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
       ///////////////VALIDACION///////////////////////////
         // VALIDACION FECHA
      boolean validado = true;
      int trimIn = 0, trim = 0, anyo = 0;
       BDControl bd = new BDControl();
      //Validar fecha
      
      int anno = comboFecha.getCalendar().get(Calendar.YEAR);
      int mes = comboFecha.getCalendar().get(Calendar.MARCH);
      mes++;
      int dia = comboFecha.getCalendar().get(Calendar.DAY_OF_MONTH);
      
      if(mes<=3 && mes >0){
          trimIn = 1;
      }
      if(mes>3 && mes<=6){
          trimIn = 2;
      }
      if(mes>6 && mes<=9){
          trimIn = 3;
      }
      if(mes>9 && mes<=12){
          trimIn = 4;
      }
                
        try {
            String periodo = bd.leePeriodo();
            
            String t =periodo.substring(8);
            String a =periodo.substring(0,4);
            trim = Integer.parseInt(t);
            anyo = Integer.parseInt(a);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex, "Error al leer periodo",0);
            validado=false;
        }
        if(trimIn != trim || anno != anyo){
            JOptionPane.showMessageDialog(null,"Esta fecha no corresponde al período en el que estamos trabajando", "Atención",0);
            validado=false;
        }
        if(comboCli.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Debes seleccionar un cliente", "Aatención",1);
            validado=false;
        }
        if(textNumFra.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Debes Introducir un  número de factura", "Aatención",1);
            validado=false;
        }
        if(textTotal.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Debes Introducir un  importe", "Aatención",1);
            validado=false;
        }
        //////////FIN DE VALIDACION////////////////////////////////
        //////////SI LA VALIDACION ES CORRECTA GRABAMOS LOS DATOS/////////////////
        if(validado == true){
            if(radioEditFra.isSelected()==true){
                borraFacturaEmit();
            }
            
            int id_cliente = Integer.valueOf(textNumCli.getText()) ;
             String diaS = String.format("%02d",dia) ;
             String mesS = String.format("%02d", mes);
            String fechaFactura = diaS+"/"+mesS+"/"+String.valueOf(anno);
            String fechaOrden = String.valueOf(anno) + mesS + diaS;
            String numFactura = textNumFra.getText();
            String id_cuenta="700000";
            float base = Float.valueOf(textBase.getText()) ;
            float tipo = Float.valueOf(textTipo.getText());
            float iva = Float.valueOf(textIva.getText());
            float total = Float.valueOf(textTotal.getText());
            String vto = textVto.getText();
            float material = 0;
            float mo =0;
            if(textMat.getText().length()!= 0){
                material = Float.valueOf(textMat.getText());
            }
            
            if(textMO.getText().length() != 0){
                 mo = Float.valueOf(textMO.getText());
            }
            
             
          
                    
          try {
              bd.guardaFactEmitidas(id_cliente,fechaFactura,fechaOrden,numFactura,id_cuenta,base,tipo,iva,total,vto,material,mo);
              
          //REESCRIBE LA TABLA DE FACTURAS RECIBIDAS
          this.dispose();
             frameFrasEmitidas ffe = new frameFrasEmitidas();
             
             ffe.setVisible(true);
              
          } catch (SQLException ex) {
              JOptionPane.showMessageDialog(null,ex,"Ha sido imposible guardar la Factura", 1);
          }
            
                    
            
            
           
        }
        
    }//GEN-LAST:event_botonGuardarActionPerformed

